import fs from 'fs';
import path from 'path';

// This script reads redirects.json and generates public/_redirects
// ensuring every rule has an exact match and a trailing slash variant.

const redirectsPath = path.resolve('redirects.json');
const outputPath = path.resolve('public', '_redirects');

const redirects = JSON.parse(fs.readFileSync(redirectsPath, 'utf8'));

let outputContent = '# Auto-generated by scripts/generate-redirects.js\n';
outputContent += '# Do not edit this file directly. Edit redirects.json instead.\n\n';

for (const rule of redirects) {
  const { source, target } = rule;
  
  // Rule without trailing slash
  outputContent += `${source} ${target} 301\n`;
  
  // Rule with trailing slash
  if (!source.endsWith('/')) {
    outputContent += `${source}/ ${target} 301\n`;
  }
}

fs.writeFileSync(outputPath, outputContent, 'utf8');
console.log(`Generated ${redirects.length * 2} redirect rules to public/_redirects`);
